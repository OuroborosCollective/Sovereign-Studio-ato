import { useMemo, useState } from 'react';
import { FileItem, Card, WorkView, PipelineState, ProjectSettings } from '../types';
import { makeId, demoFiles, starterCards, defaultSettings } from '../constants';

export function useProductMagic() {
  const [repoUrl, setRepoUrl] = useState('https://github.com/OuroborosCollective/Wasd');
  const [accessKey, setAccessKey] = useState('');
  const [geminiKey, setGeminiKey] = useState('');
  const [blueprint, setBlueprint] = useState('Baue aus diesem Workflow eine klickbare Mini-App mit GitHub Explorer, Auto-Resolver, Living Preview und Agent Workspace.');
  const [cards, setCards] = useState<Card[]>(starterCards());
  const [selectedFile, setSelectedFile] = useState<FileItem>(demoFiles[0]);
  const [built, setBuilt] = useState(false);
  const [chatInput, setChatInput] = useState('Setze diesen Workflow als echtes Produkt um.');
  const [logs, setLogs] = useState<string[]>(['🚀 Sovereign Studio Auto-Resolver geladen.']);
  const [workView, setWorkView] = useState<WorkView>('editor');
  const [pipelineState, setPipelineState] = useState<PipelineState>('idle');
  const [fixLoops, setFixLoops] = useState(0);
  const [showSettings, setShowSettings] = useState(false);
  const [settings, setSettings] = useState<ProjectSettings>(defaultSettings);
  const [generatedCode, setGeneratedCode] = useState('');

  const currentCode = useMemo(() => generatedCode || `// ${selectedFile.path}\n// Sovereign Auto-Resolver Preview\n\nconst blueprint = ${JSON.stringify(blueprint, null, 2)};\n\nexport const generatedProduct = {\n  mode: 'living-preview',\n  repo: '${repoUrl}',\n  modules: ${cards.length},\n  repoMode: '${settings.repoMode}',\n  packageManager: '${settings.packageManager}',\n  linter: '${settings.linter}',\n  ready: ${built}\n};`, [generatedCode, selectedFile.path, blueprint, repoUrl, cards.length, settings.repoMode, settings.packageManager, settings.linter, built]);

  const generatedPackage = useMemo(() => JSON.stringify({ repoUrl, blueprint, cards, selectedFile: selectedFile.path, settings, generatedCode: currentCode }, null, 2), [repoUrl, blueprint, cards, selectedFile, settings, currentCode]);

  const log = (text: string) => setLogs((items) => [text, ...items].slice(0, 14));

  const generateCodeInEditor = () => {
    const pm = settings.packageManager === 'auto' ? 'detected-package-manager' : settings.packageManager;
    const lintCommand = settings.linter === 'biome' ? `${pm} biome check .` : settings.linter === 'eslint' ? `${pm} lint` : `${pm} lint || ${pm} format`;
    const installCommand = settings.repoMode === 'monorepo' ? `${pm} install --frozen-lockfile` : `${pm} install`;
    const code = `// Generated by Sovereign Studio\n// File: generated/sovereign-product/workflow.ts\n\nexport const projectProfile = {\n  repoMode: '${settings.repoMode}',\n  packageManager: '${settings.packageManager}',\n  installStrategy: '${settings.installStrategy}',\n  linter: '${settings.linter}',\n  specialization: ${JSON.stringify(settings.specialization)}\n};\n\nexport const safeCommands = {\n  install: '${installCommand}',\n  lint: '${lintCommand}',\n  test: '${pm} test',\n  build: '${pm} build'\n};\n\nexport const productModules = ${JSON.stringify(cards.map((card) => ({ title: card.title, task: card.body })), null, 2)};\n\nexport function runVisibleWorkflow() {\n  return {\n    status: 'ready-for-review',\n    blueprint: ${JSON.stringify(blueprint)},\n    next: 'publish-and-validate'\n  };\n}\n`;
    setSelectedFile({ path: 'generated/sovereign-product/workflow.ts', icon: '✨' });
    setGeneratedCode(code);
    setBuilt(true);
    setWorkView('editor');
    log('💻 Code-Agent schreibt jetzt sichtbar im Editorfenster.');
  };

  const buildProduct = () => {
    generateCodeInEditor();
    log('✨ Produkt lebt: Code, Preview und Agent-Workspace wurden erzeugt.');
  };

  const addCard = () => setCards((items) => [...items, { id: makeId(), title: 'Neues Modul', body: blueprint }]);

  const sendChat = () => {
    if (!chatInput.trim()) return;
    log(`🤖 Agent: Ich habe den Auftrag verstanden: ${chatInput}`);
    setChatInput('');
    generateCodeInEditor();
  };

  const downloadPackage = () => {
    const blob = new Blob([generatedPackage], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = `sovereign-product-${Date.now()}.json`;
    link.click();
    URL.revokeObjectURL(url);
  };

  const publishAndValidate = () => {
    setWorkView('pipeline');
    setPipelineState('publishing');
    log('🚀 Publish gestartet: Review-Branch, Dateien und Workflow-Paket werden vorbereitet.');
    window.setTimeout(() => {
      setPipelineState('validating');
      log(`🔎 Validierung läuft mit ${settings.packageManager}/${settings.linter}. Keine Single-Install-Zerstörung bei Monorepo.`);
    }, 450);
    window.setTimeout(() => {
      if (fixLoops < 1) {
        setPipelineState('failed');
        setFixLoops((count) => count + 1);
        log('❌ Workflow-Fehler erkannt: Agent springt zurück in den Editor und patcht sichtbar.');
      } else {
        setPipelineState('green');
        log('✅ Workflows grün. PR kann gemerged werden.');
      }
    }, 900);
  };

  const patchFromPipeline = () => {
    setPipelineState('patching');
    setWorkView('editor');
    const patched = `${currentCode}\n\n// AutoPatch ${fixLoops}: lint/build guard\nexport const validationPatch = {\n  reason: 'CI failure resolved by visible patch loop',\n  linter: '${settings.linter}',\n  packageManager: '${settings.packageManager}',\n  monorepoSafe: ${settings.repoMode === 'monorepo'}\n};\n`;
    setGeneratedCode(patched);
    log('🛠️ Patch sichtbar im Editor erzeugt. Danach erneut Publish & Validate drücken.');
  };

  const mergeWhenGreen = () => {
    if (pipelineState !== 'green') {
      log('⚠️ Merge blockiert: erst grüne Validierung abwarten.');
      return;
    }
    log('🎉 Merge/Patch abgeschlossen. Branch Cleanup vorbereitet.');
  };

  return {
    repoUrl, setRepoUrl,
    accessKey, setAccessKey,
    geminiKey, setGeminiKey,
    blueprint, setBlueprint,
    cards, setCards,
    selectedFile, setSelectedFile,
    built, setBuilt,
    chatInput, setChatInput,
    logs, setLogs,
    workView, setWorkView,
    pipelineState, setPipelineState,
    fixLoops, setFixLoops,
    showSettings, setShowSettings,
    settings, setSettings,
    generatedCode, setGeneratedCode,
    currentCode,
    generatedPackage,
    log,
    generateCodeInEditor,
    buildProduct,
    addCard,
    sendChat,
    downloadPackage,
    publishAndValidate,
    patchFromPipeline,
    mergeWhenGreen
  };
}
